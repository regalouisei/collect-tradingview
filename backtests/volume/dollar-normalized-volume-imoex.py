import numpy as np
import pandas as pd
import pandas_ta as pta
from backtesting import Backtest, Strategy
from backtesting.lib import crossover


class TvStrategy(Strategy):
    def init(self):
        close = pd.Series(self.data.Close)
        volume = pd.Series(self.data.Volume)
        
        # Calculate dollar-normalized volume
        dollar_volume = close * volume
        
        # Simple moving average of dollar volume for trend detection
        self.dollar_vol_ma = self.I(pta.sma, dollar_volume, length=20)
        self.dollar_volume = self.I(lambda: dollar_volume)

    def next(self):
        # Buy when dollar volume increases above its moving average (volume surge)
        if len(self.data) > 20:
            if self.dollar_volume[-1] > self.dollar_vol_ma[-1] and \
               (len(self.data) < 2 or self.dollar_volume[-2] <= self.dollar_vol_ma[-2]):
                if not self.position:
                    self.buy()
            
            # Sell when dollar volume drops below its moving average
            elif self.dollar_volume[-1] < self.dollar_vol_ma[-1] and \
                 (len(self.data) < 2 or self.dollar_volume[-2] >= self.dollar_vol_ma[-2]):
                if self.position:
                    self.sell()


